# Traffic Management System

This project is a traffic management system that uses optimization algorithms to manage and optimize traffic flow between lanes. The system leverages linear programming to optimize traffic flow based on input data.

## Features

- Load traffic flow data from a CSV file.
- Optimize traffic flow using linear programming.
- Display the optimized flow.

